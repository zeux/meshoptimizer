// corner index per Bourke's indexing (must match table above!)
static const unsigned char kCornerX[8] = {0, 1, 1, 0, 0, 1, 1, 0};
static const unsigned char kCornerY[8] = {0, 0, 0, 0, 1, 1, 1, 1};
static const unsigned char kCornerZ[8] = {0, 0, 1, 1, 0, 0, 1, 1};

// edge index per Bourke's indexing (must match table above!)
static const unsigned char kEdgeA[12] = {0, 1, 2, 3, 4, 5, 6, 7, 0, 1, 2, 3};
static const unsigned char kEdgeB[12] = {1, 2, 3, 0, 5, 6, 7, 4, 4, 5, 6, 7};

struct Voxel
{
	float px, py, pz;
	float w;
};

static float measureGrid(const float* vertex_positions_data, size_t vertex_count, size_t vertex_positions_stride, int resolution, float* out_offset)
{
	size_t vertex_stride_float = vertex_positions_stride / sizeof(float);

	float minv[3] = {FLT_MAX, FLT_MAX, FLT_MAX};
	float maxv[3] = {-FLT_MAX, -FLT_MAX, -FLT_MAX};

	for (size_t i = 0; i < vertex_count; ++i)
	{
		const float* v = vertex_positions_data + i * vertex_stride_float;

		for (int j = 0; j < 3; ++j)
		{
			float vj = v[j];

			minv[j] = minv[j] > vj ? vj : minv[j];
			maxv[j] = maxv[j] < vj ? vj : maxv[j];
		}
	}

	float extent = 0.f;

	extent = (maxv[0] - minv[0]) < extent ? extent : (maxv[0] - minv[0]);
	extent = (maxv[1] - minv[1]) < extent ? extent : (maxv[1] - minv[1]);
	extent = (maxv[2] - minv[2]) < extent ? extent : (maxv[2] - minv[2]);

	// note: we rescale model extents to [0..resolution - 2], because the first and last voxel are used as empty padding
	float scale = extent == 0 ? 0.f : (resolution - 2) / extent;

	out_offset[0] = minv[0];
	out_offset[1] = minv[1];
	out_offset[2] = minv[2];

	return scale;
}

static void voxelize(unsigned char* grid, Voxel* voxels, const unsigned int* voxel_rows, const unsigned int* indices, size_t index_count, const float* vertex_positions, size_t vertex_count, size_t vertex_positions_stride, int resolution, float scale, const float offset[3])
{
	(void)vertex_count;

	size_t vertex_stride_float = vertex_positions_stride / sizeof(float);

	for (size_t i = 0; i < index_count; i += 3)
	{
		unsigned int a = indices[i + 0], b = indices[i + 1], c = indices[i + 2];
		assert(a < vertex_count && b < vertex_count && c < vertex_count);

		const float* va = vertex_positions + a * vertex_stride_float;
		const float* vb = vertex_positions + b * vertex_stride_float;
		const float* vc = vertex_positions + c * vertex_stride_float;

		float ex = vb[0] - va[0], ey = vb[1] - va[1], ez = vb[2] - va[2];
		float fx = vc[0] - va[0], fy = vc[1] - va[1], fz = vc[2] - va[2];
		float gx = vc[0] - vb[0], gy = vc[1] - vb[1], gz = vc[2] - vb[2];

		// use maximum edge length to establish sampling rate
		// TODO: this is wasteful for thin triangles
		float el = sqrtf(ex * ex + ey * ey + ez * ez);
		float fl = sqrtf(fx * fx + fy * fy + fz * fz);
		float gl = sqrtf(gx * gx + gy * gy + gz * gz);

		float max_edge = el > fl ? el : fl;
		max_edge = max_edge > gl ? max_edge : gl;

		// we target 2 samples per voxel edge which should be enough to hit all voxels at any rotation
		int samples = int(max_edge * scale * 2.f);
		samples = samples > 1 ? samples : 1;
		samples = samples < resolution * 2 ? samples : resolution * 2;

		float sx = va[0] - offset[0], sy = va[1] - offset[1], sz = va[2] - offset[2];
		float sr = 1.f / float(samples);

		for (int u = 0; u <= samples; ++u)
			for (int v = 0; v <= samples - u; ++v)
			{
				float su = float(u) * sr, sv = float(v) * sr;
				float px = sx + su * ex + sv * fx;
				float py = sy + su * ey + sv * fy;
				float pz = sz + su * ez + sv * fz;

				int x = int(px * scale);
				int y = int(py * scale);
				int z = int(pz * scale);

				// safety: rounding errors and non-finite inputs may produce out of bounds coordinates, so we clamp them
				// TODO: codegen
				x = (x < 0) ? 0 : (x > resolution - 2 ? resolution - 2 : x);
				y = (y < 0) ? 0 : (y > resolution - 2 ? resolution - 2 : y);
				z = (z < 0) ? 0 : (z > resolution - 2 ? resolution - 2 : z);

				size_t row = (y + 1) + size_t(resolution) * (z + 1);
				size_t idx = (x + 1) + size_t(resolution) * row;

				if (voxels)
				{
					assert(grid[idx]);
					Voxel& vox = voxels[voxel_rows[row] + (grid[idx] - 1)];

					vox.px += px;
					vox.py += py;
					vox.pz += pz;
					vox.w += 1.f;
				}
				else
				{
					grid[idx] = 1;
				}
			}
	}
}

static size_t emitVertex(float* destination, size_t index, int x, int y, int z, int edge, const unsigned char* grid, const Voxel* voxels, const unsigned int* voxel_rows, int resolution, float scale, const float offset[3])
{
	int a = kEdgeA[edge], b = kEdgeB[edge];
	int ax = kCornerX[a], ay = kCornerY[a], az = kCornerZ[a];
	int bx = kCornerX[b], by = kCornerY[b], bz = kCornerZ[b];

	// TODO: store directional edges in the table so that we know which corner is occupied
	unsigned char ag = grid[(x + ax) + size_t(resolution) * ((y + ay) + size_t(resolution) * (z + az))];
	unsigned char bg = grid[(x + bx) + size_t(resolution) * ((y + by) + size_t(resolution) * (z + bz))];
	assert((ag != 0) + (bg != 0) == 1); // one of the corners must be occupied

	int dx = (ag == 0) ? bx : ax;
	int dy = (ag == 0) ? by : ay;
	int dz = (ag == 0) ? bz : az;

	size_t row = (y + dy) + size_t(resolution) * (z + dz);
	size_t idx = (x + dx) + size_t(resolution) * row;

	if (destination)
	{
		assert(grid[idx]);
		const Voxel& vox = voxels[voxel_rows[row] + (grid[idx] - 1)];

		destination[index * 3 + 0] = vox.px / vox.w + offset[0];
		destination[index * 3 + 1] = vox.py / vox.w + offset[1];
		destination[index * 3 + 2] = vox.pz / vox.w + offset[2];
	}

	return idx;
}

static size_t polygonize(float* destination, size_t max_triangle_count, const unsigned char* grid, const Voxel* voxels, const unsigned int* voxel_rows, int resolution, float scale, const float offset[3])
{
	size_t result = 0;

	for (int z = 0; z < resolution - 1; ++z)
		for (int y = 0; y < resolution - 1; ++y)
			for (int x = 0; x < resolution - 1; ++x)
			{
				int cube = 0;

				for (int i = 0; i < 8; ++i)
				{
					int ix = x + kCornerX[i];
					int iy = y + kCornerY[i];
					int iz = z + kCornerZ[i];

					size_t index = ix + size_t(resolution) * (iy + size_t(resolution) * iz);
					cube |= (grid[index] == 0) << i;
				}

				if (cube == 0 || cube == 0xff)
					continue;

				const signed char* tris = kTriangleTable[cube];

				for (int i = 0; tris[i] != -1; i += 3)
				{
					int ea = tris[i + 0], eb = tris[i + 2], ec = tris[i + 1];

					// note: we only emit the triangle if we have space for it, but we reject degenerate triangles purely based on vertex codes
					// this results in consistent capacity estimation, as result advances the same way regardless of whether triangle data is written
					float* target = (destination && result < max_triangle_count) ? destination : NULL;

					size_t ca = emitVertex(target, result * 3 + 0, x, y, z, ea, grid, voxels, voxel_rows, resolution, scale, offset);
					size_t cb = emitVertex(target, result * 3 + 1, x, y, z, eb, grid, voxels, voxel_rows, resolution, scale, offset);
					size_t cc = emitVertex(target, result * 3 + 2, x, y, z, ec, grid, voxels, voxel_rows, resolution, scale, offset);

					result += (ca != cb) && (cb != cc) && (cc != ca); // degenerate triangle
				}
			}

	return result;
}

